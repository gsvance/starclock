info = """StarClock (v3, August 2013, now implemented using Python)
Created by Greg Vance, Connecticut College

A simple application for observational astronomy that displays:

 - current local time
 - current universal coordinated time
 - current Julian date
 - current local sidereal time

All values are recalculated and updated in real time.

Julian date is calculated using the method found at:
   http://en.wikipedia.org/wiki/Julian_day

Local sidereal time is calculated using the approximation outlined at:
   http://aa.usno.navy.mil/faq/docs/GAST.php

Accurate calculation of LST requires the user's longitude.
This value must be provided for the program.

Program includes terminal and GUI display formats."""
